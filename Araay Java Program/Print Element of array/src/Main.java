//5) Java Program to print the elements of an array
public class Main {
    public static void main(String[] args) {
        System.out.println("Print Array element");
        int [] a={1,2,34,45,6};
        for (int i=0;i<a.length;i++)
        {
            System.out.print(a[i]+" ");
        }
    }
}